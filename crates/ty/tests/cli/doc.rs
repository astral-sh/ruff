#[test]
fn stdlib_signature_links_follow_resolved_definitions() -> anyhow::Result<()> {
    let case = CliTest::with_files([(
        "pkg/__init__.py",
        r#"
        import dataclasses
        from typing import Any as TypingAny
        from unresolved_dependency import ExternalType

        class Any:
            """Local class that shadows typing.Any."""

        class Holder:
            object: object

        def local(value: Any) -> Any:
            """Use the local class."""

        def external(value: TypingAny) -> TypingAny:
            """Use the imported stdlib type alias."""

        def unresolved(value: ExternalType) -> ExternalType:
            """Do not invent docs.python.org links for unresolved external types."""

        def uses_path(value: int = dataclasses.field(default=1)) -> None:
            """Use a dotted stdlib path in the signature."""
        "#,
    )])?;

    assert_cmd_snapshot!(case.ty_command().arg("doc").arg("--color").arg("never"), @"
    success: true
    exit_code: 0
    ----- stdout -----
     Documenting project (<temp_dir>/)
        Finished documentation in 0.000s
       Generated <temp_dir>/target/doc/project/index.html

    ----- stderr -----
    ");

    let output = case.root().join("target/doc/project/pkg");
    let module = std::fs::read_to_string(output.join("index.html"))?;
    assert_contains_all(
        &module,
        &[
            "value: <a class=\"ty\"",
            "class.Any.html\">Any</a>",
            "<a class=\"bi\" href=\"https://docs.python.org/3/library/typing.html#typing.Any\">TypingAny</a>",
            "value: ExternalType",
            "<a class=\"bi\" href=\"https://docs.python.org/3/library/dataclasses.html\">dataclasses</a>.<a class=\"bi\" href=\"https://docs.python.org/3/library/dataclasses.html#dataclasses.field\">field</a>",
        ],
    );
    assert_contains_none(
        &module,
        &[
            "typing.html#typing.Any\">Any</a>",
            "ty_extensions.html#ty_extensions.ExternalType",
        ],
    );

    let holder = std::fs::read_to_string(output.join("class.Holder.html"))?;
    assert_contains_all(
        &holder,
        &[
            "<a class=\"bi\" href=\"https://docs.python.org/3/library/functions.html#object\">object</a>",
        ],
    );
    assert!(!output.join("fn.local.html").exists());
    assert!(!output.join("fn.external.html").exists());
    assert!(!output.join("fn.unresolved.html").exists());
    assert!(!output.join("fn.uses_path.html").exists());

    Ok(())
}

