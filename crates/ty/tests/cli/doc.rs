use insta_cmd::assert_cmd_snapshot;

use crate::CliTest;

fn assert_contains_all(content: &str, needles: &[&str]) {
    for needle in needles {
        assert!(content.contains(needle), "missing `{needle}`");
    }
}

fn assert_contains_none(content: &str, needles: &[&str]) {
    for needle in needles {
        assert!(!content.contains(needle), "unexpected `{needle}`");
    }
}

fn assert_appears_before(content: &str, left: &str, right: &str) {
    let left = content
        .find(left)
        .unwrap_or_else(|| panic!("missing `{left}`"));
    let right = content
        .find(right)
        .unwrap_or_else(|| panic!("missing `{right}`"));
    assert!(left < right, "expected `{left}` to appear before `{right}`");
}

